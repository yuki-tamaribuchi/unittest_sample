class calculate():

    def __init__(self,x,y):
        self.x=x
        self.y=y


    def add(self):
        return self.x+self.y

    def sub(self):
        return self.x-self.y

    def mul(self):
        return self.x*self.y

    def div(self):
        return self.x//self.y

    def mod(self):
        return self.x%self.y

    def double_plus_one(self):
        return self.x*2+self.y*2+1
